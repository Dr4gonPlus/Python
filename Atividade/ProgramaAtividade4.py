# 4 Crie um algoritmo que solicite ao usuário uma cor (vermelho, verde, azul) e exiba uma mensagem correspondente à cor escolhida.

Cor = str(input('Informe uma cor:'))

if (Cor == 'vermelho'):
    print('vermelho')
elif (Cor == 'verde'):
    print('verde')
elif (Cor == 'azul'):
    print('azul')
else :
    print('Cor invalida')