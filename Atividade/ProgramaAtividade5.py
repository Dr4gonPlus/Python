# 5 Faça um programa que solicite ao usuário dois números e verifique se ambos são pares.

numero1 = int(input('informe o numero: \n'))
numero2 = int(input('informe o numero: \n'))

if numero1 % 2 == 0:

    print('numero é par')
else:
    print('numero é impar')

if numero2 % 2 == 0:

    print('numero é par')
else:
    print('numero é impar')
