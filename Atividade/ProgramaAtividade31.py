# 31 Escreva um algoritmo que peça ao usuário um número de 1 a 5 e verifique se ele é igual a 3.

numero = int(input('digite um numero de 1 a 5: \n'))



if (numero == 3):
    print("o numero e igual a 3")
elif (numero >= 4):
    print('maior que 3')
elif (numero <= 2):
    print('numero menor que 3')
elif ('numero >= 6'):
    print('numero invalido')
